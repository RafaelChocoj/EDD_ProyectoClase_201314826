    private void com_prodItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_com_prodItemStateChanged
        if (evt.getStateChange() == ItemEvent.SELECTED) {
            String idprud = com_prod.getSelectedItem().toString();
            int int_produc;
            try {
                int_produc = Integer.parseInt(idprud);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Debe de ingresar un numero","info",JOptionPane.ERROR_MESSAGE);
                return;
            }
            //JOptionPane.showMessageDialog(null, idcat,"info",JOptionPane.INFORMATION_MESSAGE);
            
            //NodeProducto nod_cat;
            nod_cat = ProyectoClase.mat_productos.existeProducto(int_produc);
            if (nod_cat != null) {
                t_producto.setText(nod_cat.nombrear);
            } else {
                t_producto.setText("");
            }

        }
    }//GEN-LAST:event_com_prodItemStateChanged

